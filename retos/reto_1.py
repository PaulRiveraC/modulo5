alto = float(input("Ingrese el valor del Alto del rectángulo: "))
ancho = float(input("Ingrese el valor del Ancho del rectángulo: "))

area = alto * ancho

perimetro = 2 * (alto + ancho)

print("\nResultados:")
print(f"Área del rectángulo: {area}")
print(f"Perímetro del rectángulo: {perimetro}")
print(f"Superficie del rectángulo: {area}")